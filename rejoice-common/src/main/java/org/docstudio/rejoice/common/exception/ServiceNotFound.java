package org.docstudio.rejoice.common.exception;

public class ServiceNotFound extends RuntimeException {

	public ServiceNotFound() {
		super();
	}

	public ServiceNotFound(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public ServiceNotFound(String arg0) {
		super(arg0);
	}

	public ServiceNotFound(Throwable arg0) {
		super(arg0);
	}
	
	

}
